-- =============================================================================
-- Bookstore Schema for PostgreSQL (Relational)
--
-- Showcases: normalization, foreign keys, constraints, many-to-many
-- relationships via join tables, CHECK constraints, views, and indexes.
--
-- Data: 120 books, 40 authors, 13 genres, 25 customers, 355 reviews,
--        61 orders. Generated from data/books.json + data/customers.json.
-- =============================================================================

-- Authors are their own entity, normalized out of books.
CREATE TABLE IF NOT EXISTS authors (
  id         SERIAL PRIMARY KEY,
  name       TEXT NOT NULL,
  bio        TEXT,
  born_year  INT
);

-- Genres as a lookup table enable consistent categorization.
CREATE TABLE IF NOT EXISTS genres (
  id    SERIAL PRIMARY KEY,
  name  TEXT NOT NULL UNIQUE
);

-- Books reference a genre via foreign key. Price uses NUMERIC for precision.
CREATE TABLE IF NOT EXISTS books (
  id             SERIAL PRIMARY KEY,
  title          TEXT NOT NULL,
  isbn           TEXT NOT NULL UNIQUE,
  genre_id       INT NOT NULL REFERENCES genres(id),
  price          NUMERIC(6,2) NOT NULL CHECK (price >= 0),
  published_date DATE,
  in_stock       BOOLEAN NOT NULL DEFAULT true
);

-- Many-to-many: a book can have multiple authors, an author can write many books.
CREATE TABLE IF NOT EXISTS book_authors (
  book_id   INT NOT NULL REFERENCES books(id),
  author_id INT NOT NULL REFERENCES authors(id),
  PRIMARY KEY (book_id, author_id)
);

-- Customers are separate from the catalog.
CREATE TABLE IF NOT EXISTS customers (
  id         SERIAL PRIMARY KEY,
  email      TEXT NOT NULL UNIQUE,
  name       TEXT NOT NULL,
  joined_at  TIMESTAMP NOT NULL DEFAULT now()
);

-- Reviews tie a customer to a book with a rating constraint.
CREATE TABLE IF NOT EXISTS reviews (
  id          SERIAL PRIMARY KEY,
  book_id     INT NOT NULL REFERENCES books(id),
  customer_id INT NOT NULL REFERENCES customers(id),
  rating      INT NOT NULL CHECK (rating BETWEEN 1 AND 5),
  body        TEXT,
  created_at  TIMESTAMP NOT NULL DEFAULT now(),
  UNIQUE (book_id, customer_id) -- one review per customer per book
);

-- Orders track purchases. Status is constrained to known values.
CREATE TABLE IF NOT EXISTS orders (
  id          SERIAL PRIMARY KEY,
  customer_id INT NOT NULL REFERENCES customers(id),
  status      TEXT NOT NULL DEFAULT 'pending'
                CHECK (status IN ('pending', 'shipped', 'delivered', 'cancelled')),
  created_at  TIMESTAMP NOT NULL DEFAULT now()
);

-- Order line items link orders to books with quantity.
CREATE TABLE IF NOT EXISTS order_items (
  order_id   INT NOT NULL REFERENCES orders(id),
  book_id    INT NOT NULL REFERENCES books(id),
  quantity   INT NOT NULL CHECK (quantity > 0),
  unit_price NUMERIC(6,2) NOT NULL,
  PRIMARY KEY (order_id, book_id)
);

-- A view that demonstrates the power of JOINs: aggregate book info in one query.
CREATE OR REPLACE VIEW book_details AS
SELECT
  b.id,
  b.title,
  b.isbn,
  g.name AS genre,
  b.price,
  b.published_date,
  b.in_stock,
  ARRAY_AGG(DISTINCT a.name) AS authors,
  COALESCE(AVG(r.rating), 0) AS avg_rating,
  COUNT(DISTINCT r.id) AS review_count
FROM books b
JOIN genres g        ON g.id = b.genre_id
JOIN book_authors ba ON ba.book_id = b.id
JOIN authors a       ON a.id = ba.author_id
LEFT JOIN reviews r  ON r.book_id = b.id
GROUP BY b.id, g.name;

-- Indexes for common query patterns.
CREATE INDEX IF NOT EXISTS idx_books_genre     ON books(genre_id);
CREATE INDEX IF NOT EXISTS idx_reviews_book    ON reviews(book_id);
CREATE INDEX IF NOT EXISTS idx_orders_customer ON orders(customer_id);

